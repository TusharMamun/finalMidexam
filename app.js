document.getElementById("cart-bton").addEventListener('click', function(){
    const cart = document.getElementById("prdct-price")
    const text = cart.innerText
    const textTo = parseFloat(text)
 
    

    const total= document.getElementById("total-amount")
    const totAm = total.innerText
    const amuntText = parseFloat(totAm)
const TotalCartAmount = textTo + amuntText 
total.innerText = TotalCartAmount 



})
// first product end


